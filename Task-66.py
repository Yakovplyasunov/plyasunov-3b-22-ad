# Написать программу, которая принимает на вход строку и выводит на экран количество различных подстрок строки, начинающихся и заканчивающихся одним и тем же символом.

# Определение функции count_substrings(s), которая принимает строку s в качестве аргумента.
def count_substrings(s):
# Инициализация переменной count со значением 0. Эта переменная будет использоваться для подсчета подстрок.    
    count = 0
# Создание словаря char_freq для хранения частоты каждого символа в строке.
    char_freq = {}
# Цикл for char in s: для перебора каждого символа char в строке s
    for char in s:
# Условный оператор if char in char_freq:, который проверяет, есть ли символ char в словаре char_freq.
        if char in char_freq:
# Если символ char уже есть в словаре, увеличиваем его частоту на 1: char_freq[char] += 1.
            char_freq[char] += 1
# В противном случае, добавляем символ char в словарь с частотой 1: char_freq[char] = 1.
        else:
            char_freq[char] = 1
# Цикл for freq in char_freq.values(): для перебора частот freq каждого символа в словаре char_freq.
    for freq in char_freq.values():
# Для каждой частоты freq, вычисляем количество различных подстрок, начинающихся и заканчивающихся этим символом, используя формулу count += (freq * (freq - 1)) // 2.
        count += (freq * (freq - 1)) // 2
# Возвращаем общее количество найденных подстрок: return count.
    return count

# Пример использования

# Определение переменной input_string со значением "abcab".
input_string = "abcab"
# Вызов функции count_substrings(input_string) и сохранение результата в переменную result.
result = count_substrings(input_string)
# Вывод значения переменной result на экран с помощью функции print(result).
print(result)
